    <h3>ARCHIVO CENTRAL - MINISTERIO DEL AMBIENTE</h3>
 </CENTER>
    
<Blockquote>
El Archivo Central del Ministerio del Ambiente es el área encargada del acopio y custodia de la gestión documental generada 
por las diversas unidades orgánicas que la conforman, garantizando la conservación, protección y difusión de los mismos. 
<br>
Nuestro valioso Patrimonio Documental es fuente de investigación para diversos usuarios desde profesionales, investigadores,
estudiantes y público en general que desea conocer asuntos en materia.
<p></p>

<p><u><b>Misión</b></u>
    <br>
Planificar, organizar, dirigir, normar, coordinar y ejecutar las actividades archivísticas a nivel institucional.
Brindar un servicio eficiente y eficaz a todos los usuarios internos y externos que lo necesiten. Salvaguardando los fondos documentales que se custodian.
</p>

<p><u><b>Visión</b></u>
    <br>
Consolidar el Archivo Central del Ministerio del Ambiente como una entidad líder, y como referente en la gestión de archivos, contribuyendo al desarrollo archivístico a nivel nacional.
</p>

<u><b>Servicios</b></u>
<br>
<li>Búsqueda</li>
<li>Préstamo documental</li>
<li>Atención vía E-Mail</li>

